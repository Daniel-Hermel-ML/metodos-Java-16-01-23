package aula_medotos;

public class Classe1 {
    //Método Public
    public static void metodoPublico(){
        System.out.println("Método Público Classe1 - Classe Testa Métodos");
    }

    //Método Friendly
    static void metodoFriendly(){
        System.out.println("Método Friendly Classe1 - Classe Testa Métodos");
    }

    //Método Protected
    protected static void metodoProtegido(){
        System.out.println("Método Protegido Classe1 - Classe Testa Métodos");
    }

    //Método Private
    private static void metodoPrivado(){
        System.out.println("Método Privado Classe1 - Classe Testa Métodos");
    }
}
